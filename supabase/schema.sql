drop policy if exists "conversations_select" on public.conversations;
create policy "conversations_select"
on public.conversations
for select
to authenticated
using (
  auth.uid() in (user_one_id, user_two_id)
  and not public.is_blocked_between(user_one_id, user_two_id)
);

drop policy if exists "conversations_insert" on public.conversations;
create policy "conversations_insert"
on public.conversations
for insert
to authenticated
with check (
  auth.uid() in (user_one_id, user_two_id)
  and user_one_id <> user_two_id
  and not public.is_blocked_between(user_one_id, user_two_id)
);

drop policy if exists "messages_select" on public.messages;
create policy "messages_select"
on public.messages
for select
to authenticated
using (
  exists (
    select 1
    from public.conversations
    where conversations.id = conversation_id
      and auth.uid() in (conversations.user_one_id, conversations.user_two_id)
      and not public.is_blocked_between(conversations.user_one_id, conversations.user_two_id)
  )
);

drop policy if exists "messages_insert" on public.messages;
create policy "messages_insert"
on public.messages
for insert
to authenticated
with check (
  sender_id = auth.uid()
  and exists (
    select 1
    from public.conversations
    where conversations.id = conversation_id
      and auth.uid() in (conversations.user_one_id, conversations.user_two_id)
      and not public.is_blocked_between(conversations.user_one_id, conversations.user_two_id)
  )
  and (
    select count(*)
    from public.messages as existing_messages
    where existing_messages.conversation_id = messages.conversation_id
      and existing_messages.sender_id = auth.uid()
  ) < 10
);

